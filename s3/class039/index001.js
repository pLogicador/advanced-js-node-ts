// `While` and `do while` (loop structures)
// Example 01
let index = 0;

while (index <= 10) {
    console.log(index);
    index++;    // be careful with the infinite loop
}

// Example 02
const varName = 'pLogicador';
let i = 0;

while (i <= varName.length-1) {
    console.log(varName[i]);
    i++;   
}
