/*
    Evite Palavras Reservadas:
    -   Evite o uso de palavras reservadas do JS para nomear suas constantes, pois isso pode causar conflitos e erros. 

    Escolha Nomes Significativos:
    -   Atribua nomes significativos e descritivos às suas constantes para facilitar a compreensão do código. 

    Inicie com Letras e Não com Números:
    -   Certifique-se de que o nome da constante comece com uma letra e não com um número.

    Evite Espaços ou Traços:
    -   Não utilize espaços ou traços em nomes de constantes.

    Adote o CamelCase:
    -   Utilize a prática de CamelCase para formar nomes de constantes. Nomes compostos por várias palavras são 
        escritos sem espaços e cada palavra subsequente inicia com uma letra maiúscula.

    Considerações Case-Sensitive:
    -   Lembre-se de que JS é uma linguagem case-sensitive. Isso significa que minhaConstante e MinhaConstante são 
        tratadas como constantes distintas. Preste atenção à consistência no uso de maiúsculas e minúsculas ao se 
        referir a constantes.

    Evite modificar o valor de uma constantes

    Declare  e inicialize uma constante de imediato !!
    UTILIZE 'const' AOA INVÉS DE 'var' !!
*/
