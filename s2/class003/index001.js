// console.log -- podemos inserir valores literais ou não inline


// Texto == String
console.log('Hello World!');    // Aspas Simples 
console.log("Hello World!");    // Aspas Duplas
console.log(`Hello World!`);    // Crase - template strings
console.log('"Hello" `World`!');    // Combinação de aspas simples, duplas e crase

// Number == Número
console.log(123456, 15.549898); // Números inteiros e de ponto flutuante